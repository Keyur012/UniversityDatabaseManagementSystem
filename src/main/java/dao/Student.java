package dao;

import java.sql.Date;

public class Student {
	private String studentId,studentName,studentEmail,studentPincode,studentCity,studentBranch,studentProgramme,studentClass,studentPortalPassword;
	private int studentAge;
	private Date studentDob;
	
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getStudentEmail() {
		return studentEmail;
	}
	public void setStudentEmail(String studentEmail) {
		this.studentEmail = studentEmail;
	}
	public String getStudentPincode() {
		return studentPincode;
	}
	public void setStudentPincode(String studentPincode) {
		this.studentPincode = studentPincode;
	}
	public String getStudentCity() {
		return studentCity;
	}
	public void setStudentCity(String studentCity) {
		this.studentCity = studentCity;
	}
	public String getStudentBranch() {
		return studentBranch;
	}
	public void setStudentBranch(String studentBranch) {
		this.studentBranch = studentBranch;
	}
	public String getStudentProgramme() {
		return studentProgramme;
	}
	public void setStudentProgramme(String studentProgramme) {
		this.studentProgramme = studentProgramme;
	}
	public String getStudentClass() {
		return studentClass;
	}
	public void setStudentClass(String studentClass) {
		this.studentClass = studentClass;
	}
	public String getStudentPortalPassword() {
		return studentPortalPassword;
	}
	public void setStudentPortalPassword(String studentPortalPassword) {
		this.studentPortalPassword = studentPortalPassword;
	}
	public int getStudentAge() {
		return studentAge;
	}
	public void setStudentAge(int studentAge) {
		this.studentAge = studentAge;
	}
	public Date getStudentDob() {
		return studentDob;
	}
	public void setStudentDob(Date studentDob) {
		this.studentDob = studentDob;
	}
	
	
	
}
