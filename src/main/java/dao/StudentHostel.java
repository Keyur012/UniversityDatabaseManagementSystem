package dao;

public class StudentHostel {
	private int hostelId;
	private int studentRoomNumber;
	private String studentId,studentHostelId;
	
	public int getHostelId() {
		return hostelId;
	}
	public void setHostelId(int hostelId) {
		this.hostelId = hostelId;
	}
	public int getStudentRoomNumber() {
		return studentRoomNumber;
	}
	public void setStudentRoomNumber(int studentRoomNumber) {
		this.studentRoomNumber = studentRoomNumber;
	}
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getStudentHostelId() {
		return studentHostelId;
	}
	public void setStudentHostelId(String studentHostelId) {
		this.studentHostelId = studentHostelId;
	}
	
	
}
